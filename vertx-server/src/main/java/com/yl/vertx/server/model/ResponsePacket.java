public class ResponsePacket<T> {

    //状态码
    private Integer code;

    //返回信息
    private String msg;

    //返回数据
    private T data;

    private ResponsePacket(Integer code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    private ResponsePacket(Integer code, String msg, T data) {
        this.code = code;
        this.msg = msg;
        this.data = data;
    }

    @Override
    public String toString() {
        return Json.encode(this);
    }

}
